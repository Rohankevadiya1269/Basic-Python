# Python has a rich set of built-in functions that provide essential functionality for various tasks. Here's a list of some commonly used built-in functions in Python:

# Type Conversion Functions:

# int(x): Converts x to an integer.
# float(x): Converts x to a floating-point number.
# str(x): Converts x to a string.
# list(x): Converts x to a list.
# tuple(x): Converts x to a tuple.

# Math Functions:

# abs(x): Returns the absolute value of x.
# pow(x, y): Returns x to the power of y.
# round(x, n): Rounds x to n decimal places.

# Sequence Functions:

# len(x): Returns the length of x (number of items in a sequence).
# max(iterable): Returns the largest item in an iterable.
# min(iterable): Returns the smallest item in an iterable.
# sum(iterable): Returns the sum of all items in an iterable.

# Input/Output Functions:

# input(prompt): Reads a line from the console.
# print(value, ...): Prints the specified values to the console.

# List/Dictionary/Set Functions:

# sorted(iterable): Returns a new sorted list from the elements of an iterable.
# list(iterable): Creates a new list from an iterable.
# dict(**kwargs): Creates a new dictionary from keyword arguments.
# set(iterable): Creates a new set from an iterable.

# String Functions:

# len(s): Returns the length of the string.
# str.upper(): Returns a copy of the string with all characters in uppercase.
# str.lower(): Returns a copy of the string with all characters in lowercase.
# str.capitalize(): Returns a copy of the string with the first character capitalized.
# str.replace(old, new): Returns a copy of the string with occurrences of old replaced by new.
# str.split(sep): Returns a list of words in the string separated by the specified separator.

# Logical/Comparison Functions:

# bool(x): Converts x to a Boolean (True or False).
# all(iterable): Returns True if all elements of the iterable are true.
# any(iterable): Returns True if any element of the iterable is true.

# Other Useful Functions:

# range(start, stop, step): Returns a sequence of numbers from start to (stop - 1) with the specified step.
# enumerate(iterable): Returns an iterator of tuples containing indices and values from the iterable.
# zip(iterable1, iterable2, ...): Returns an iterator of tuples, where the i-th tuple contains the i-th element from each of the input iterables.

# Type Checking Functions:

# isinstance(obj, class_or_tuple): Returns True if the object is an instance of the specified class or tuple of classes.

# These are just a few examples, and Python provides many more built-in functions. You can find more details in the Python documentation: Built-in Functions.


